    void retrieve() {
        String query = "SELECT*FROM patient_registration "
                + "Inner join address on patient_registration.F_Address = Add_code "
                + "Inner join account on patient_registration.F_Account= Acc_id ";

        DefaultTableModel dtm = (DefaultTableModel) patreg_data.getModel();
        dtm.setRowCount(0);

        try {
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {

                patID = rs.getString("Patient_Id");
                lname = rs.getString("Patient_lname");
                fname = rs.getString("Patient_fname");
                dateR = rs.getString("Date_admission");
                dateB = rs.getString("Birthdate");
                gender = rs.getString("Gender");
                weight = rs.getString("Weight");
                height = rs.getString("Height");
                address = rs.getString("Barangay") + ", " + rs.getString("Municipality") + ", " + rs.getString("Province");
                account = rs.getString("Acc_Name");

                dtm.addRow(new Object[]{patID, lname, fname, dateR, dateB, gender, weight, height, address, account});
            }

        } catch (SQLException ex) {
            Logger.getLogger(Patient_Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

