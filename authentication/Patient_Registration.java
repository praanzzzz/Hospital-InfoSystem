    private void exportexcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportexcelActionPerformed
        // TODO add your handling code here:
        try {
            Workbook wb = new XSSFWorkbook();
            Sheet sheet = wb.createSheet("Sample");
            Row row;
            Cell cell;

            CellStyle style1 = wb.createCellStyle();

            style1.setAlignment(HorizontalAlignment.CENTER);
            style1.setVerticalAlignment(VerticalAlignment.CENTER);

            //HEADER
            row = sheet.createRow(1);
            sheet.addMergedRegion(new CellRangeAddress(1, 1, 0, 10));
            cell = row.createCell(0);
            cell.setCellValue("LIST OF REGISTERED PATIENTS");
            cell.setCellStyle(style1);

            //number of column
            row = sheet.createRow(2);
            for (int i = 0; i < 10; i++) {
                cell = row.createCell(i);
                cell.setCellValue(patreg_data.getColumnName(i));
                cell.setCellStyle(style1);
            }

            //rows
            row = sheet.createRow(3);
            for (int i = 0; i < patreg_data.getRowCount(); i++) {
                row = sheet.createRow(i + 3);

                for (int j = 0; j < 10; j++) {
                    cell = row.createCell(j);
                    cell.setCellValue((String) patreg_data.getValueAt(i, j));

                    cell.setCellStyle(style1);
                }
            }

            //OUTPUT
            FileOutputStream out = new FileOutputStream(new File("C:\\Users\\franz\\Downloads\\Registered_Patients\\patreg.xlsx"));

            wb.write(out);
            wb.close();
            out.close();
            JOptionPane.showMessageDialog(null, "Exported successfully!");
            openFile("C:\\Users\\franz\\Downloads\\Registered_Patients\\patreg.xlsx");
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_exportexcelActionPerformed

